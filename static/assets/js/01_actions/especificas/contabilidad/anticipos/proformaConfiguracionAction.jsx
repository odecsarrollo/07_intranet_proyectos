import {PROFORMA_CONFIGURACION_TYPES as TYPES} from '../../../00_types';
import {
    fetchListGet,
    updateObject,
    deleteObject,
} from '../../../00_general_fuctions'

const current_url_api = 'contabilidad_anticipos_proforma_anticipo_configuracion';
export const deleteProformaConfiguracion = (id, options_action = {}) => {
    return (dispatch) => {
        const dispatches = (response) => {
            dispatch({type: TYPES.delete, payload: id})
        };
        const options = {dispatches, ...options_action, dispatch_method: dispatch};
        return deleteObject(current_url_api, id, options);
    }
};
export const fetchProformaConfiguracion = (options_action = {}) => {
    return (dispatch) => {
        const dispatches = (response) => {
            dispatch({type: TYPES.fetch_all, payload: response})
        };
        const {limpiar_coleccion = true} = options_action;
        const options = {
            dispatches, ...options_action,
            dispatch_method: dispatch,
            clear_action_type: limpiar_coleccion ? TYPES.clear : null
        };
        return fetchListGet(current_url_api, options);
    }
};

export const clearProformaConfiguracion = () => {
    return (dispatch) => {
        dispatch({type: TYPES.clear});

    }
};
export const updateProformaConfiguracion = (id, values, options_action = {}) => {
    return (dispatch) => {
        const dispatches = (response) => {
            dispatch({type: TYPES.update, payload: response})
        };
        const options = {dispatches, ...options_action, dispatch_method: dispatch};
        return updateObject(current_url_api, id, values, options);
    }
};